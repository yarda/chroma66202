#include <inttypes.h>
#include <time.h>

typedef struct {
	int fd;
	int opened;
	int noanswer;

	uint32_t my_addr;
	uint8_t target_channel;
	uint32_t transit_addr;
} tipmi;

typedef struct {
	int curr;
	int min;
	int max;
	int avg;
	struct tm timestamp;
	int samp_period;
	int state;
} tpwr;

extern int openipmi_open(tipmi *ipmi);
extern void openipmi_close(tipmi *ipmi);
extern int openipmi_pwr_rd(tipmi *ipmi, tpwr *pwr);
